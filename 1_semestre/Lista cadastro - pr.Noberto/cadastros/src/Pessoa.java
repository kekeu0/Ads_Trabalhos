public class Pessoa {
    String nome;
    String matricula;
    String rg;
    String cpf;
    String endereco;

    public Pessoa(String nome, String matricula, String rg, String cpf, String endereco) {
        this.nome = nome;
        this.matricula = matricula;
        this.rg = rg;
        this.cpf = cpf;
        this.endereco = endereco;
    }
}
